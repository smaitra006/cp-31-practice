inline ll gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
inline ll lcm(ll a, ll b) { return (a / gcd(a, b)) * b; }

inline ll power(ll base, ll exp, ll mod = MOD)
{
  ll res = 1;
  base %= mod;
  while (exp > 0)
  {
    if (exp & 1)
      res = (res * base) % mod;
    base = (base * base) % mod;
    exp >>= 1;
  }
  return res;
}

// =============================================================================
//  SOLVE FUNCTION
// =============================================================================
void solve()
{
  int n, k;
  cin >> n >> k;
  vvi a(n, vi(n));
  for (int i = 0; i < n; i++)
  {
    for (int j = 0; j < n; j++)
    {
      cin >> a[i][j];
    }
  }
  int cnt = 0;
  for (int i = 0; i < n; i++)
  {
    for (int j = 0; j < n; j++)
    {
      if (a[i][j] != a[n - i - 1][n - j - 1])
        cnt++;
    }
  }
  cnt /= 2;
  if (cnt > k)
    cout << "NO" << endl;
  else
  {
    k -= cnt;
    if (n & 1)
      cout << "YES" << endl;
    else if (k & 1)
      cout << "NO" << endl;
    else
      cout << "YES" << endl;
  }
}

